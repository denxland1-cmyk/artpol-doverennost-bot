"""Конвертер русских числительных прописью → int.

Покрывает 1..999 (для номенклатуры в доверенностях этого достаточно с запасом).
"""

UNITS = {
    "ноль": 0,
    "один": 1, "одна": 1, "одно": 1,
    "два": 2, "две": 2,
    "три": 3, "четыре": 4, "пять": 5, "шесть": 6, "семь": 7,
    "восемь": 8, "девять": 9,
}

TEENS = {
    "десять": 10, "одиннадцать": 11, "двенадцать": 12, "тринадцать": 13,
    "четырнадцать": 14, "пятнадцать": 15, "шестнадцать": 16,
    "семнадцать": 17, "восемнадцать": 18, "девятнадцать": 19,
}

TENS = {
    "двадцать": 20, "тридцать": 30, "сорок": 40, "пятьдесят": 50,
    "шестьдесят": 60, "семьдесят": 70, "восемьдесят": 80, "девяносто": 90,
}

HUNDREDS = {
    "сто": 100, "двести": 200, "триста": 300, "четыреста": 400,
    "пятьсот": 500, "шестьсот": 600, "семьсот": 700,
    "восемьсот": 800, "девятьсот": 900,
}

ALL = {**UNITS, **TEENS, **TENS, **HUNDREDS}


def propisi_to_int(text: str) -> int | None:
    """«Шестнадцать» → 16, «Двадцать пять» → 25, «Сто восемьдесят семь» → 187.

    Возвращает None если не удалось распознать.
    """
    if not text:
        return None
    words = text.lower().strip().split()
    total = 0
    found_any = False
    for w in words:
        w = w.strip(".,;:")
        if w in ALL:
            total += ALL[w]
            found_any = True
    return total if found_any else None
